value = int(input("Enter a number: "))

if value == 3:
    print("Value is 3")
    print("Value is an odd number")
    print("Value is a prime number")
elif value == 4:
    print("Value is 4")
    print("Value is an even number")
    print("Value is NOT a prime number")
elif value == 9:
    print("Value is 9")
    print("Value is an odd number")
    print("Value is NOT a prime number")
else:
    print("Value is NOT 3 or 4 or 9!")